package pe.com.mimp.modelo;

public class Violencia {
	
	int codigoViolencia;
	String descripcionViolencia;
	int flagMensaje;
	String mensaje;
	int codigoTipoViolencia;
	
	public int getCodigoViolencia() {
		return codigoViolencia;
	}
	public void setCodigoViolencia(int codigoViolencia) {
		this.codigoViolencia = codigoViolencia;
	}
	public String getDescripcionViolencia() {
		return descripcionViolencia;
	}
	public void setDescripcionViolencia(String descripcionViolencia) {
		this.descripcionViolencia = descripcionViolencia;
	}
	public int getFlagMensaje() {
		return flagMensaje;
	}
	public void setFlagMensaje(int flagMensaje) {
		this.flagMensaje = flagMensaje;
	}
	public String getMensaje() {
		return mensaje;
	}
	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}
	public int getCodigoTipoViolencia() {
		return codigoTipoViolencia;
	}
	public void setCodigoTipoViolencia(int codigoTipoViolencia) {
		this.codigoTipoViolencia = codigoTipoViolencia;
	}
	
}
