package pe.com.mimp.modelo;

public class VinculosFamiliares {
	
	int codigoVinculosAfectivos;
	String descripcionVinculos;
	
	public int getCodigoVinculosAfectivos() {
		return codigoVinculosAfectivos;
	}
	public void setCodigoVinculosAfectivos(int codigoVinculosAfectivos) {
		this.codigoVinculosAfectivos = codigoVinculosAfectivos;
	}
	public String getDescripcionVinculos() {
		return descripcionVinculos;
	}
	public void setDescripcionVinculos(String descripcionVinculos) {
		this.descripcionVinculos = descripcionVinculos;
	}
	
}
