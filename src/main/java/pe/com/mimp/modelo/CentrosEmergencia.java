package pe.com.mimp.modelo;

public class CentrosEmergencia {
	
	int codigoCem;
	int codigoTipoCentrosEmergencia;
	String nombreCem;
	String codigoUbigeo;
	
	public int getCodigoCem() {
		return codigoCem;
	}
	public void setCodigoCem(int codigoCem) {
		this.codigoCem = codigoCem;
	}
	public int getCodigoTipoCentrosEmergencia() {
		return codigoTipoCentrosEmergencia;
	}
	public void setCodigoTipoCentrosEmergencia(int codigoTipoCentrosEmergencia) {
		this.codigoTipoCentrosEmergencia = codigoTipoCentrosEmergencia;
	}
	public String getNombreCem() {
		return nombreCem;
	}
	public void setNombreCem(String nombreCem) {
		this.nombreCem = nombreCem;
	}
	public String getCodigoUbigeo() {
		return codigoUbigeo;
	}
	public void setCodigoUbigeo(String codigoUbigeo) {
		this.codigoUbigeo = codigoUbigeo;
	}
	
}
